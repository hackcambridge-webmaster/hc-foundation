              and welcoming environment.
            </p>

            <p>
              Harassment includes but is not limited to offensive verbal or
              written comments related to gender, age, sexual orientation, disability,
              physical appearance, body size, race, religion, social class,
              economic status, veteran status, sexual images, deliberate
              intimidation, stalking, following, harassing photography or
              recording, sustained disruption of talks or other events,
              inappropriate physical contact, and unwelcome sexual attention.
              If what you’re doing is making someone feel uncomfortable,
              that counts as harassment and is enough reason to stop doing it.
            </p>

            <p>
              Participants asked to stop any harassing behavior are expected
              to comply immediately.
            </p>

            <p>
              Sponsors, judges, mentors, volunteers, organizers, MLH staff,
              and anyone else at the event are also subject to the anti-harassment
              policy. In particular, attendees should not use sexualised images,
              activities, or other material both in their hacks and during the event.
              Booth staff (including volunteers) should not use sexualised
              clothing/uniforms/costumes, or otherwise create a sexualised
              environment.
            </p>

            <p>
              If a participant engages in harassing behavior,
              MLH may take any action it deems appropriate,
              including warning the offender or expulsion
              from the event with no eligibility for reimbursement
              or refund of any type.
            </p>

            <p>
              If you are being harassed, notice that someone else is being harassed,
              or have any other concerns, please contact MLH using the
              reporting procedures defined below.
            </p>

            <p>
              MLH representatives will be happy to help participants contact
              campus security or local law enforcement, provide escorts,
              or otherwise assist those experiencing harassment to
              feel safe for the duration of the event. We value your
              attendance.
            </p>

            <p>
